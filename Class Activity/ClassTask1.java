class ClassTask1{
  public static void main(String args[]){
	System.out.println("Even Numbers");
      for(int i=0;i<=50;i++)
       {
        if(i%2==0)
        {
        System.out.println(i);
        }
      }
      System.out.println("Odd Number");
      for(int j=0;j<=50;j++)
      {
       if(j%2==1)
	{
	System.out.println(j);
	 }
      }
  }
}